package Model;

import java.io.Serializable;

/**
 *
 * @author HP
 */
public class Supplier implements Serializable{
    private int id;
    private String name;

    public Supplier() {
        super();
    }

    public Supplier(String name) {
        super();
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
